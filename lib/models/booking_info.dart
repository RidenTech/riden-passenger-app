class BookingInfo {
  final String date,
      price,
      pickLabel,
      pickAddress,
      pickTime,
      dropLabel,
      dropAddress,
      dropTime;
  BookingInfo({
    required this.date,
    required this.price,
    required this.pickLabel,
    required this.pickAddress,
    required this.pickTime,
    required this.dropLabel,
    required this.dropAddress,
    required this.dropTime,
  });
}
